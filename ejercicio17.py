# Lista del ejercicio 7 (números enteros multiplicados por 5)
numeros_enteros = [2, 4, 6]
multiplicados = [num * 5 for num in numeros_enteros]
print("Lista original de números multiplicados:", multiplicados)


conteo = multiplicados.count(30)
print("Cantidad de veces que aparece el número 30 en la lista multiplicada:", conteo)
