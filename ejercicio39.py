# Lista con nombres de materias de la universidad
materias = ["Matemáticas", "Física", "Química", "Historia", "Biología"]
print("Lista original de materias:", materias)

# Eliminar y obtener el último elemento usando .pop()
ultima_materia = materias.pop()
print("Última materia eliminada de la lista:", ultima_materia)
print("Lista de materias después de eliminar el último elemento:", materias)
