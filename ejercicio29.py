# Lista con nombres de posibles mascotas
mascotas = ["Firulais", "Max", "Luna", "Rocky", "Simba", "Toby"]
print("Lista original de nombres de mascotas:", mascotas)

# Eliminar un nombre específico de la lista usando .remove()
mascotas.remove("Luna")
print("Lista después de eliminar 'Luna':", mascotas)
